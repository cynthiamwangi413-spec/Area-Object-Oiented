public class Person {
    private String name;
    private String status;
    private int year;


    public void setName(String name){
        this.name = name;
    }
    public void setStatus(String status){
        this.status=status;

    }
    public void setYear(int year){
        this.year=year;
    }
    public String getName() {
        return name;
    }
   public String getStatus(){
        return status;
    }
  public int getYear(){
        return year;
    }

}
