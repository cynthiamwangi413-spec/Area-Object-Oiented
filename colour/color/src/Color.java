public class Color {
    public static void main(String[] args) {
        char Color='C';
        switch(Color){
            case 'R':
                System.out.println('r');
                break;
            case 'B':
                System.out.println('b');
                break;
            case 'G':
                System.out.println('g');
                break;
            default:
                System.out.println("Invalid color");
                break;

        }
    }
}